#include "Int.hpp"

#include "../../../../../src/cs-utils/filesystem.hpp"

namespace csp::visualquery {

////////////////////////////////////////////////////////////////////////////////////////////////////

const std::string Int::sName = "Int";

////////////////////////////////////////////////////////////////////////////////////////////////////

std::string Int::sSource() {
  return cs::utils::filesystem::loadToString(
      "../share/resources/nodes/csp-visual-query/Int.js");
}

////////////////////////////////////////////////////////////////////////////////////////////////////

std::unique_ptr<Int> Int::sCreate() {
  return std::make_unique<Int>();
}

////////////////////////////////////////////////////////////////////////////////////////////////////

std::string const& Int::getName() const {
  return sName;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void Int::process() {
  writeOutput("value", mValue);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void Int::onMessageFromJS(nlohmann::json const& message) {

  // The message sent via CosmoScout.sendMessageToCPP() contains the selected number.
  mValue = message;
  // Whenever the user entered a number, we write it to the output socket by calling the process()
  // method. Writing the output will not trigger a graph reprocessing right away, it will only queue
  // up the connected nodes for being processed in the next update step (and only if the value
  // actually changed).
  process();
}

////////////////////////////////////////////////////////////////////////////////////////////////////

nlohmann::json Int::getData() const {
  return {{"value", mValue}};
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void Int::setData(nlohmann::json const& json) {
  mValue = json["value"];
}

////////////////////////////////////////////////////////////////////////////////////////////////////

} // namespace csp::visualquery